def run(dry_run=True):
    con = get_con()
    rows = fetchall(con, "SELECT id, award, award_year, title, isbn13 FROM award_books WHERE status='確認済'")

    updated = 0
    not_found = []
    for (award, year, isbn, title) in ISBN_DATA:
        matches = [r for r in rows
                   if r["award"] == award
                   and r["award_year"] == year
                   and r["title"].replace("　", " ").strip() == title.replace("　", " ").strip()]
        if not matches:
            not_found.append((award, year, title))
            continue
        for r in matches:
            if r["isbn13"] == isbn:
                continue  # 既に同じISBNが入っている
            if dry_run:
                print(f"[DRY] UPDATE id={r['id']} {award} {year} 「{title}」 isbn13={isbn}")
            else:
                execute(con, "UPDATE award_books SET isbn13=%s WHERE id=%s", (isbn, r["id"]))
            updated += 1

    if not dry_run:
        con.commit()
    con.close()

    print(f"\n{'[DRY RUN] ' if dry_run else ''}更新件数: {updated}")
    if not_found:
        print(f"\nDBに見つからなかった作品 ({len(not_found)}件):")
        for award, year, title in not_found:
            print(f"  {award} {year} 「{title}」")


if __name__ == "__main__":
    dry = "--dry-run" in sys.argv or "-n" in sys.argv
    if dry:
        print("=== ドライラン（実際には更新しません） ===\n")
    run(dry_run=dry)
